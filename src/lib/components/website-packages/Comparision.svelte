<script lang="ts">
  let activeCol: number | null = $state(null);
  let lockedCol: number | null = $state(null);
  let showDiffOnly = $state(false);

  const plans = ["Starter", "Intermediate", "Enterprise"];

  const sections = [
    {
      title: "",
      rows: [
        { label: "P", tooltip: "Total number of pages designed & developed", values: [5, 10, 20] },
      ]
    },
    {
      title: "Website Structure",
      rows: [
        { label: "Pages Included", tooltip: "Total number of pages designed & developed", values: [5, 10, 20] },
        { label: "Custom Design", tooltip: "Fully custom UI/UX tailored to your brand", values: [false, true, true] },
        { label: "Mobile Responsive", values: [true, true, true] },
        { label: "CMS Integration", tooltip: "Manage content easily (WordPress / Headless)", values: [false, true, true] }
      ]
    },
    {
      title: "Performance & SEO",
      rows: [
        { label: "Speed Optimization", values: ["Basic", "Advanced", "Advanced+"] },
        { label: "SEO Ready Structure", values: [true, true, true] },
        { label: "Core Web Vitals Optimization", values: [false, true, true] }
      ]
    },
    {
      title: "Design & Experience",
      rows: [
        { label: "Animations & Interactions", values: ["Basic", "Premium", "Next-Level"] },
        { label: "Conversion-Focused Design", values: [false, true, true] },
        { label: "UI/UX Strategy", values: [false, true, true] }
      ]
    },
    {
      title: "Integrations & Support",
      rows: [
        { label: "3rd Party Integrations", values: [2, 5, "Unlimited"] },
        { label: "Analytics Setup", values: [true, true, true] },
        { label: "Post-Launch Support", values: ["7 Days", "14 Days", "30 Days"] }
      ]
    }
  ];

  function display(val: any) {
    if (val === true) return "✔";
    if (val === false) return "✖";
    return val;
  }

  function isDifferent(values: any[]) {
    return new Set(values).size > 1;
  }

  function setCol(i: number) {
    lockedCol = lockedCol === i ? null : i;
  }
</script>

<section class="bg-[#020f05] text-white py-32">

  <div class="max-w-7xl mx-auto px-6 md:px-20">

    <!-- HEADER -->
    <div class="flex flex-col md:flex-row justify-between items-start md:items-center mb-14 gap-6">

      <div>
        <h2 class="text-[42px] md:text-[64px] font-semibold">
          What you actually get
        </h2>
        <p class="text-gray-400 mt-3 max-w-xl">
          Transparent breakdown of what each package includes.
        </p>
      </div>

      <button
        on:click={() => showDiffOnly = !showDiffOnly}
        class="px-5 py-2 rounded-full border border-white/10 text-xs tracking-widest uppercase hover:border-lime-400 transition"
      >
        {showDiffOnly ? "Show All" : "Show Differences Only"}
      </button>

    </div>

    <!-- 🔥 MOBILE -->
    <div class="md:hidden space-y-6">

      {#each plans as plan, i}

        <div class="p-5 rounded-2xl border border-white/10 bg-black/30">

          <h3 class="text-lg font-semibold mb-4">{plan}</h3>

          {#each sections as section}

            <div class="mt-4">
              <div class="text-xs text-lime-400 mb-2 uppercase tracking-widest">
                {section.title}
              </div>

              {#each section.rows as row}
                {#if section.title === "Website Structure" || !showDiffOnly || isDifferent(row.values)}

                  <div class="flex justify-between py-2 border-b border-white/5 text-sm">
                    <span class="text-gray-300">{row.label}</span>
                    <span class="text-lime-400">{display(row.values[i])}</span>
                  </div>

                {/if}
              {/each}

            </div>

          {/each}

        </div>

      {/each}

    </div>

    <!-- 🔥 DESKTOP -->
    <div class="hidden md:block overflow-x-auto">

      <div class="min-w-[800px]">

        <!-- HEADER -->
        <div class="grid grid-cols-4 gap-4 mb-6 sticky top-24 bg-[#020f05]/90 backdrop-blur-xl py-4">

          <div></div>

          {#each plans as plan, i}
            <!-- svelte-ignore a11y_click_events_have_key_events -->
            <!-- svelte-ignore a11y_no_static_element_interactions -->
            <!-- svelte-ignore event_directive_deprecated -->
            <div
              class="text-center p-3 border border-white/10 rounded-xl cursor-pointer transition"
              class:bg-lime-400={(lockedCol ?? activeCol) === i}
              on:mouseenter={() => activeCol = i}
              on:mouseleave={() => activeCol = null}
              on:click={() => setCol(i)}
            >
              {plan}
            </div>
          {/each}

        </div>

        <!-- DATA -->
        {#each sections as section}

          <div class="text-lime-400 text-xs tracking-widest mt-8 mb-3">
            {section.title}
          </div>

          {#each section.rows as row}

            {#if !showDiffOnly || isDifferent(row.values)}

              <div
                class="grid grid-cols-4 gap-4 py-3 border-b border-white/5 transition"
                class:opacity-30={showDiffOnly && !isDifferent(row.values)}
                >

                <div class="text-gray-300 text-sm">
                  {row.label}
                </div>

                {#each row.values as val, j}
                  <div
                    class="text-center text-sm transition"
                    class:text-lime-400={(lockedCol ?? activeCol) === j}
                  >
                    {display(val)}
                  </div>
                {/each}

              </div>

            {/if}

          {/each}

        {/each}

      </div>

    </div>

  </div>

</section>